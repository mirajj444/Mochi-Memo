function displayHomepageBlog() {
  const sortedPosts = [...blog_posts].sort(
    (a, b) => new Date(b.date) - new Date(a.date)
  );

  const featuredPost = sortedPosts[0];
  const regularPosts = sortedPosts.slice(1, 5);

  const featuredPostHTML = `
    <a href="post.html?id=${featuredPost.id}" class="featured-post">
      <div class="featured-image" style="background-image: url('${featuredPost.image}')">
        <div class="featured-content">
          <span class="post-date">${featuredPost.date}</span>
          <h3 class="post-title">${featuredPost.title}</h3>
        </div>
      </div>
    </a>
  `;

  let regularPostsHTML = "";
  regularPosts.forEach((post) => {
    regularPostsHTML += `
      <a href="post.html?id=${post.id}" class="regular-post">
        <div class="post-thumbnail" style="background-image: url('${post.image}')"></div>
        <div class="post-info">
          <span class="post-date">${post.date}</span>
          <h4 class="post-title">${post.title}</h4>
        </div>
      </a>
    `;
  });

  const blogSectionHTML = `
    <div class="blog-header">
      <h2 class="big-title">Blog Posts</h2>
      <a href="blog.html" class="see-more-btn">Read more</a>
    </div>
    <div class="blog-container">
      ${featuredPostHTML}
      <div class="regular-posts-grid">
        ${regularPostsHTML}
      </div>
    </div>
  `;

  const blogContainer = document.getElementById("blog-container");
  if (blogContainer) {
    blogContainer.innerHTML = blogSectionHTML;
  }
}

document.addEventListener("DOMContentLoaded", displayHomepageBlog);

function displayBlogPage() {
  const sortedPosts = [...blog_posts].sort(
    (a, b) => new Date(b.date) - new Date(a.date)
  );

  let postsHTML = '<div class="blog-page-grid">';

  sortedPosts.forEach((post) => {
    postsHTML += `
      <div class="blog-post-card">
        <div class="blog-post-image" style="background-image: url('${post.image}')"></div>
        <div class="blog-post-content">
          <span class="post-date">${post.date}</span>
          <h3 class="post-title">${post.title}</h3>
          <a href="post.html?id=${post.id}" class="read-more-btn">Read More</a>
        </div>
      </div>
    `;
  });

  postsHTML += "</div>";

  const blogContainer = document.getElementById("blog-container");
  if (blogContainer) {
    blogContainer.innerHTML = postsHTML;
  }
}

if (window.location.pathname.includes("blog.html")) {
  document.addEventListener("DOMContentLoaded", displayBlogPage);
}

function displayBlogPost() {
  const urlParams = new URLSearchParams(window.location.search);
  const postId = parseInt(urlParams.get("id"));

  const post = blog_posts.find((p) => p.id === postId);

  document.getElementById(
    "post-image"
  ).style.backgroundImage = `url('${post.image}')`;
  document.getElementById("post-date").textContent = post.date;
  document.getElementById("post-title").textContent = post.title;

  const descriptionHTML = post.description
    .map((paragraph) => `<p>${paragraph}</p>`)
    .join("");

  document.getElementById("post-description").innerHTML = descriptionHTML;

  const currentIndex = blog_posts.findIndex((p) => p.id === postId);

  if (currentIndex > 0) {
    const prevPost = blog_posts[currentIndex - 1];
    document.getElementById("prev-post").href = `post.html?id=${prevPost.id}`;
  } else {
    document.getElementById("prev-post").style.display = "none";
  }

  if (currentIndex < blog_posts.length - 1) {
    const nextPost = blog_posts[currentIndex + 1];
    document.getElementById("next-post").href = `post.html?id=${nextPost.id}`;
  } else {
    document.getElementById("next-post").style.display = "none";
  }

  document.title = `Mochi Memo | ${post.title}`;
}

if (window.location.pathname.includes("post.html")) {
  document.addEventListener("DOMContentLoaded", displayBlogPost);
}
